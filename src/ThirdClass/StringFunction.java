public class StringFunction {
    public static void main(String[] args) {
        String numString = "22";  // This is a valid string
        int convertedFromString = Integer.parseInt(numString);  // Converts to integer
        System.out.println(convertedFromString);  // Prints the integer value
        System.out.println(convertedFromString);  // Prints the integer value again
    }
}
